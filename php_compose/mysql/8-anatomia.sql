            UPPER(CONCAT(L.NOM_NOMBRE, L.NOM_APEPAT, L.NOM_APEMAT)) LIKE CONCAT('%', UPPER(search_term), '%')
        )
    ORDER BY P.DATE_INICIOREGISTRO;
