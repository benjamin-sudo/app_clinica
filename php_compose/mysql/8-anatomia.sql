        ADMIN.GG_TPROFESIONAL G
    WHERE
        P.NUM_FICHAE = L.NUM_FICHAE AND
        P.COD_RUTPRO = G.COD_RUTPRO AND
        (P.COD_EMPRESA = cod_empresa OR P.COD_ESTABLREF = cod_empresa) AND
