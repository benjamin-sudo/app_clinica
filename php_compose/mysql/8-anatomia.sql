CREATE PROCEDURE ADMIN.LIS_CANCER_NOTIFICADO (
    IN V_COD_EMPRESA VARCHAR(10),
    IN V_FECHA_INICIO DATE,
    IN V_FECHA_FINAL DATE
)
BEGIN
    DECLARE CURSOR_FINISHED INT DEFAULT 0;
    DECLARE V_EMPRESA VARCHAR(10);

    -- Declarar cursor
    DECLARE EMPRESA_CURSOR CURSOR FOR 
        SELECT '303' UNION SELECT '301' UNION SELECT '304' UNION SELECT '300' UNION
        SELECT '103' UNION SELECT '101' UNION SELECT '102' UNION SELECT '105' UNION
        SELECT V_COD_EMPRESA;

    -- Declarar el handler para el cursor
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET CURSOR_FINISHED = 1;

    -- Crear una tabla temporal para almacenar las empresas derivadas
    CREATE TEMPORARY TABLE IF NOT EXISTS TEMP_EMPRESAS (
        COD_EMPRESA VARCHAR(10)
    );

    -- Limpiar la tabla temporal
    DELETE FROM TEMP_EMPRESAS;

    -- Insertar las empresas derivadas en la tabla temporal
    IF V_COD_EMPRESA IN ('029') THEN
        INSERT INTO TEMP_EMPRESAS VALUES (V_COD_EMPRESA);
    ELSEIF V_COD_EMPRESA IN ('100') THEN
        OPEN EMPRESA_CURSOR;
        REPEAT
            FETCH EMPRESA_CURSOR INTO V_EMPRESA;
            IF NOT CURSOR_FINISHED THEN
                INSERT INTO TEMP_EMPRESAS VALUES (V_EMPRESA);
            END IF;
        UNTIL CURSOR_FINISHED END REPEAT;
        CLOSE EMPRESA_CURSOR;
    ELSE
        INSERT INTO TEMP_EMPRESAS VALUES 
        ('318'), ('104'), ('108'), ('107'), ('302'), (V_COD_EMPRESA);
    END IF;

    -- Seleccionar los resultados
    SELECT 
        P.ID_SOLICITUD_HISTO AS ID_SOLICITUD, 
        CONCAT(L.COD_RUTPAC, '-', L.COD_DIGVER) AS RUTPACIENTE,
        L.IND_TISEXO AS IND_TISEXO,
        L.COD_RUTPAC AS COD_RUTPAC,
        FLOOR(TIMESTAMPDIFF(MONTH, L.FEC_NACIMI, CURRENT_DATE) / 12) AS NUMEDAD,
        DATE_FORMAT(L.FEC_NACIMI, '%d-%m-%Y') AS NACIMIENTO,
        FLOOR(TIMESTAMPDIFF(MONTH, L.FEC_NACIMI, CURRENT_DATE) / 12) AS EDAD,
        CONCAT(UPPER(L.NOM_NOMBRE), ' ', UPPER(L.NOM_APEPAT), ' ', UPPER(L.NOM_APEMAT)) AS NOMBRE_COMPLETO,
        CONCAT(SUBSTRING(L.NOM_NOMBRE, 1, 1), '.', UPPER(L.NOM_APEPAT), ' ', UPPER(L.NOM_APEMAT)) AS TXTNOMCIRUSMALL,
        CONCAT(UPPER(L.NOM_NOMBRE), ' ', UPPER(L.NOM_APEPAT), ' ', UPPER(SUBSTRING(L.NOM_APEMAT, 1, 1))) AS TXTPRIMERNOMBREAPELLIDO,
        L.NUM_FICHAE AS NUM_FICHAE,  
        CONCAT(UPPER(G.NOM_NOMBRE), ' ', UPPER(G.NOM_APEPAT), ' ', UPPER(G.NOM_APEMAT)) AS PROFESIONAL,
        CONCAT(G.NOM_APEPAT, ' ', G.NOM_APEMAT, ' ', G.NOM_NOMBRE) AS PROFESIONAL_2,
        CONCAT(G.COD_RUTPRO, '-', G.COD_DIGVER) AS RUT_PROFESIOAL,
        P.IND_CONF_CANCER,
        P.NUM_NOF_CANCER,
        P.IND_NOTIFICACANCER,
        CASE P.IND_TIPO_BIOPSIA
            WHEN '1' THEN 'SI'
            WHEN '2' THEN 'CONTEMPORANEA'
            WHEN '3' THEN 'DIFERIDA'
            WHEN '4' THEN 'BIOPSIA + CITOLOGÍA'
            WHEN '6' THEN 'CITOLOGÍA PAP'
            WHEN '5' THEN 'SOLO CITOLOGÍA'
            ELSE 'NO INFORMADO'
        END AS TIPO_DE_BIOPSIA
    FROM 
        ADMIN.GG_TPROFESIONAL A,
        ADMIN.GG_TGPACTE L,
        ADMIN.GG_TPROFESIONAL G,
        ADMIN.PB_SOLICITUD_HISTO P
    WHERE
        P.DATE_INICIOREGISTRO BETWEEN V_FECHA_INICIO AND V_FECHA_FINAL
        AND P.COD_EMPRESA IN (SELECT COD_EMPRESA FROM TEMP_EMPRESAS)
        AND A.COD_RUTPRO = P.COD_RUTPRO
        AND P.NUM_FICHAE = L.NUM_FICHAE
        AND P.COD_RUTPRO = G.COD_RUTPRO
        AND P.IND_CONF_CANCER IN (1)
        AND P.IND_ESTADO IN (1)
    ORDER BY 
        P.DATE_INICIOREGISTRO;
    
    -- Eliminar la tabla temporal
    DROP TEMPORARY TABLE TEMP_EMPRESAS;
END //

